    void loadTable(DefaultTableModel model) {
        String sql = "SELECT id, name, quantity, price FROM products"; // Query to fetch data

        try {
            dbConnect db = new dbConnect();
            db.connect();
            Connection conn = db.getConnection();

            PreparedStatement pstmt = conn.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            model.setRowCount(0); // Clear existing data

            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                int stock = rs.getInt("quantity");
                double price = rs.getDouble("price");

                // Add row to the table
                model.addRow(new Object[]{id, name, stock, price});
            }

            rs.close();
            pstmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void saveReportAsCSV(String filePath) {
        String customerName = JOptionPane.showInputDialog(null, "Enter Customer Name:");
        if (customerName == null || customerName.trim().isEmpty()) {
            return;
        }

        String phoneNo = JOptionPane.showInputDialog(null, "Enter Customer Phone No:");
        if (phoneNo == null || phoneNo.trim().isEmpty()) {
            return;
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath))) {
            DefaultTableModel reportModel = (DefaultTableModel) reportTable.getModel();

            // Write customer info
            writer.write("Customer Name," + customerName.trim());
            writer.newLine();
            writer.write("Customer Phone," + phoneNo.trim());
            writer.newLine();
            writer.newLine(); // extra line before the table

            // Write column headers
            for (int col = 0; col < reportModel.getColumnCount(); col++) {
                writer.write(reportModel.getColumnName(col));
                if (col < reportModel.getColumnCount() - 1) {
                    writer.write(",");
                }
            }
            writer.newLine();

            // Write rows
            for (int row = 0; row < reportModel.getRowCount(); row++) {
                for (int col = 0; col < reportModel.getColumnCount(); col++) {
                    writer.write(reportModel.getValueAt(row, col).toString());
                    if (col < reportModel.getColumnCount() - 1) {
                        writer.write(",");
                    }
                }
                writer.newLine();
            }

            JOptionPane.showMessageDialog(null, "Report saved successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error saving report!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

