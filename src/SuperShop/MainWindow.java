    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = stockTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to update.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Assuming column 0 = ID, column 1 = price
        int id = (int) stockTable.getValueAt(selectedRow, 0);
        double currentPrice = (double) stockTable.getValueAt(selectedRow, 3);

        String newPriceStr = JOptionPane.showInputDialog(this, "Set new price:", currentPrice);
        if (newPriceStr == null) {
            return; // User cancelled
        }
        try {
            double newPrice = Double.parseDouble(newPriceStr);

            dbConnect db = new dbConnect();
            db.connect();
            Connection con = db.getConnection();

            String sql = "UPDATE products SET price = ? WHERE id = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setDouble(1, newPrice);
            pst.setInt(2, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Price updated successfully.");
            db.closeConnection();
            loadTable((DefaultTableModel) stockTable.getModel());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid price entered.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

