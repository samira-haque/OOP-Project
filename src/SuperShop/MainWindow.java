    private void addDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDataActionPerformed
        // TODO add your handling code here:
        // Get the selected row from sellTable
        DefaultTableModel sellModel = (DefaultTableModel) sellTable.getModel();
        DefaultTableModel basketModel = (DefaultTableModel) basketTable.getModel();

        int selectedRow = sellTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select a product!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Fetch selected product details safely
        int id = (int) sellModel.getValueAt(selectedRow, 0);
        String name = (String) sellModel.getValueAt(selectedRow, 1);

        int availableQuantity;
        availableQuantity = (int) sellModel.getValueAt(selectedRow, 2);

        // Handling price safely
        double price = (double) sellModel.getValueAt(selectedRow, 3);

        // Ask user for quantity input
        String quantityStr = JOptionPane.showInputDialog(null,
                "Enter quantity for " + name + " (Available: " + availableQuantity + ")",
                "Select Quantity",
                JOptionPane.QUESTION_MESSAGE);

        if (quantityStr == null) {
            return; // User pressed cancel
        }
        try {
            int quantity = Integer.parseInt(quantityStr);

            if (quantity <= 0) {
                JOptionPane.showMessageDialog(null, "Quantity must be greater than 0!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (quantity > availableQuantity) {
                JOptionPane.showMessageDialog(null, "Insufficient stock!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Check if the item already exists in basketTable
            boolean itemExists = false;
            for (int i = 0; i < basketModel.getRowCount(); i++) {
                if ((int) basketModel.getValueAt(i, 0) == id) {
                    // Item found, update quantity and total price
                    int existingQuantity = (int) basketModel.getValueAt(i, 3);
                    int newQuantity = existingQuantity + quantity;
                    double newTotalPrice = newQuantity * price;

                    basketModel.setValueAt(newQuantity, i, 3); // Update quantity
                    basketModel.setValueAt(newTotalPrice, i, 4); // Update total price
                    itemExists = true;
                    break;
                }
            }

            if (!itemExists) {
                // Add item to basketTable if it doesn't exist
                double totalPrice = price * quantity;
                basketModel.addRow(new Object[]{id, name, price, quantity, totalPrice});
            }

            // Reduce quantity in sellTable
            sellModel.setValueAt(availableQuantity - quantity, selectedRow, 2);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid quantity entered!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addDataActionPerformed

