    private void removeItemStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeItemStockActionPerformed
        // TODO add your handling code here:
        int selectedRow = stockTable.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(null, "Please select an item to remove!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Get the item ID from the selected row (assuming ID is in column 0)
        int itemId = (int) stockTable.getValueAt(selectedRow, 0);

        // Confirm deletion
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this item?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // Connect to the database
        dbConnect db = new dbConnect();
        db.connect();
        Connection conn = db.getConnection();

        // Delete the item from the database
        String query = "DELETE FROM products WHERE id = " + itemId;

        try (Statement stmt = conn.createStatement()) {
            int rowsAffected = stmt.executeUpdate(query);

            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Item removed successfully!");

                // Remove the row from the table
                DefaultTableModel model = (DefaultTableModel) stockTable.getModel();
                model.removeRow(selectedRow);
            } else {
                JOptionPane.showMessageDialog(null, "Item not found in database!", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_removeItemStockActionPerformed

    private void addItemStockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addItemStockActionPerformed
        // TODO add your handling code here:
        String name = stockProductName.getText().trim();
        String priceText = stockPrice.getText().trim();
        String quantityText = stockQuantity.getText().trim();

        if (name.isEmpty() || priceText.isEmpty() || quantityText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Fill Up All Boxes", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        String sql = "INSERT INTO products(name, price, quantity) VALUES(?, ?, ?)";

        try {
            double price = Double.parseDouble(priceText);
            int quantity = Integer.parseInt(quantityText);

            if (price <= 0 || quantity <= 0) {
                JOptionPane.showMessageDialog(null, "Price and Quantity must be greater than 0", "Error", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            dbConnect db = new dbConnect();
            db.connect();
            Connection conn = db.getConnection();

            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, name);
            pstmt.setDouble(2, price);
            pstmt.setInt(3, quantity);

            int rowsInserted = pstmt.executeUpdate();

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Product Added Successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadTable((DefaultTableModel) stockTable.getModel());
            } else {
                JOptionPane.showMessageDialog(null, "Failed to add product.", "Error", JOptionPane.ERROR_MESSAGE);
            }

            pstmt.close();
            conn.close();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid number format for Price or Quantity", "Error", JOptionPane.INFORMATION_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addItemStockActionPerformed

